// LookupTiaohou 基于《穷通宝鉴》查表，返回日主在该月令的调候用神说明
// dayGan: 日主天干（甲乙丙丁戊己庚辛壬癸）
// monthZhi: 出生月支（子丑寅卯辰巳午未申酉戌亥）
// 未命中时返回空字符串
func LookupTiaohou(dayGan, monthZhi string) string {
	if monthMap, ok := tiaohouTable[dayGan]; ok {
		if result, ok := monthMap[monthZhi]; ok {
			return result
		}
	}
	return ""
}
