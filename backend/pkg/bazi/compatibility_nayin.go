// nayinWxPinyin 将中文五行单字映射为 wxSheng / wxKe 使用的拼音 key。
var nayinWxPinyin = map[string]string{
	"木": "mu", "火": "huo", "土": "tu", "金": "jin", "水": "shui",
}

// nayinRelation 描述两个纳音五行之间的关系：
//   "sheng" - 相生
//   "same"  - 相同
//   "ke"    - 相克
//   ""      - 输入有空字符串或未知
func nayinRelation(a, b string) string {
	if a == "" || b == "" {
		return ""
	}
	if a == b {
		return "same"
	}
	pa, pb := nayinWxPinyin[a], nayinWxPinyin[b]
	if pa == "" || pb == "" {
		return ""
	}
	if wxSheng[pa] == pb || wxSheng[pb] == pa {
		return "sheng"
	}
	if wxKe[pa] == pb || wxKe[pb] == pa {
		return "ke"
	}
	return ""
}
